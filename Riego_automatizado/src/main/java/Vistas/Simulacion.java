    private void cambiarHora(){
        //Suma+1 a la hora o se reinicia si llega a 23
        if (hora==23){
            hora = 0;
            dias_pasados+=1;
        }
        else{
            hora+=1;
        }
        
        //revisa si es de noche o de dia
        if (hora>=19 || hora<=6){
            noche = true;
            LabelDiaNoche.setText("Noche");
        }
        else{
            noche = false;
            LabelDiaNoche.setText("D√≠a");
        }
    }

    
    private void calcularTemperatura(){
        if(noche){
            temperatura = (int) (Math.random()*(21-13+1)+13);
        }
        else{
            temperatura = (int) (Math.random()*(27-18+1)+18);
        }
    }
    
