    int hora = 0;
    boolean noche=true;
    double nivel_tanque = 80;
    double temperatura;
    int dias_pasados=0;
    boolean lluvia=false;
    
    modulo[] plantas = {new modulo("Romero",4,20,40),
                        new modulo("Tomillo",3,20,40),
                        new modulo("Albahaca",5,40,60),
                        new modulo("Menta",3,20,40),
                        new modulo("Oregano",7,20,40)};
    
    private void actualizarInformacion() {
        setAllFalse();
        //Hora
        cambiarHora();
        LabelHora.setText(String.valueOf(hora));
        
        //Temperatura
        calcularTemperatura();
        labelTemperatura.setText(String.valueOf(temperatura));
        
        //plantas
        for(int i=0; i<plantas.length;i++){
            plantas[i].reducir_humedad(temperatura);
            RevisarHumedad(plantas[i]);
        }
        actualizarLabels();
        RevisarRiego();
        
        //tanque
        LabelTanque.setText(String.valueOf(nivel_tanque));
        
        if(dias_pasados==2 && noche){
            lluvia=true;
            Labellluvia.setEnabled(true);
            nivel_tanque=80;
            dias_pasados=0;
        }
        else{
            lluvia = false;
            Labellluvia.setEnabled(false);
        }
    }
    
    private void RevisarHumedad(modulo p){
        if (p.getVlr_act_h()<p.getVlr_min_h()){
            p.setRiego(true);
            nivel_tanque -= p.regar();
        }
    }
    
    private void setAllFalse(){
        plantas[0].setRiego(false);
        plantas[1].setRiego(false);
        plantas[2].setRiego(false);
        plantas[3].setRiego(false);
        plantas[4].setRiego(false);
        
    }
    
